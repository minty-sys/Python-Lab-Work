#Write a program to perform addition/subtraction/multiplication/division/floor or integer division
# and modulo division on two float values.

num1=float(input("Enter first no.\n"))
num2=float(input("Enter second no.\n"))
print("Addition is",num1+num2)
print("Subtraction is",num1-num2)
print("Multiplication is",num1*num2)
print("Division is",num1/num2)
print("Floor/Integer division is",num1//num2)
print("Modulo division is",num1%num2)

