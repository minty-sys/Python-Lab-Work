#Write a program to enter a number and display its hex and octal equivalent and also its square root.

a=int(input("Enter a number\n"))
print("The hex equivalent of entered number is",hex(a))
print("The octal equivalent of entered number is",oct(a))
print("The square root of entered number is",a**0.5)

# OUTPUT
# Enter a number
# 4
# The hex equivalent of entered number is 0x4
# The octal equivalent of entered number is 0o4
# The square root of entered number is 2.0
